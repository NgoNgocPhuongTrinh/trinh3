a = int(input("Nhập số nguyên thứ nhất: "))
b = int(input("Nhập số nguyên thứ hai: "))
c = int(input("Nhập số nguyên thứ ba: "))
print("Số lớn nhất là: ", max(a,b,c))
print("Số nhỏ nhất là: ", min(a,b,c))